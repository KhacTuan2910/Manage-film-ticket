    @Override
    public void actionPerformed(ActionEvent e) {
        var obj = e.getSource();
        if (obj.equals(btnCancel)) {
            dispose();
        } else if (obj.equals(btnClear)) {
            clearData();
        } else if (obj.equals(btnInputBookTicket)) {
            if (txtNumOfTicket.getText().equalsIgnoreCase("")
                    || txtCustomerId.getText().equalsIgnoreCase("")) {
                var msg = "Vui lòng nhập đầy đủ thông tin!";
                showMessDialog(msg);
            } else {
                inputBookTicket();
            }
//            setCost();
        } else if (obj.equals(btnShowBill)) {
            if (txtBookTimeRO.getText().isEmpty()){
                var msg = "Vui lòng đặt vé trước khi xem hóa đơn!";
                showMessDialog(msg);
            } else {
                showBill();
            }
        } else if (obj.equals(btnBookTicket)) {
            addNewBooking();
        }
    }
