    private void inputBookTicket() {
        customer = null;
        var emptyText = "";
        var customerId = txtCustomerId.getText().trim();
        if (customerId.isEmpty()) {
            var msg = "Vui lòng nhập mã khách hàng cần tìm!";
            showMessDialog(msg);
        } else {
            for (Customer c : customers) {
                if (c.getId().compareToIgnoreCase(customerId) == 0) {
                    customer = c;
                    break;
                }
            }

            if (customer != null) {
                txtCusIdReadOnly.setText(customer.getId());
                txtCusNameReadOnly.setText(customer.getFullName());
                txtCusTypeReadOnly.setText(customer.getCustomerType());
                setCost();
            } else {
                txtCusIdReadOnly.setText(emptyText);
                txtCusNameReadOnly.setText(emptyText);
                txtCusTypeReadOnly.setText(emptyText);
                var msg = "Khách hàng cần tìm hiện "
                        + "không tồn tại. Vui lòng thử lại!";
                showMessDialog(msg);
            }
        }
    }

    private void showBill() {
        ShowDetailedBill showDetailedBill = new ShowDetailedBill(this, 
                rootPaneCheckingEnabled, addShowDetailedBill());
        showDetailedBill.setVisible(true);
    }

    private void showMessDialog(String msg) {
        JOptionPane.showMessageDialog(rootPane, msg);
    }

    private void setCost() { // chỗ này cần sửa
        var roomType = txtMovieTypeReadOnly.getText();
        var cusType = txtCusTypeReadOnly.getText();
        int room2DPriceOC = 70000;
        int room2DPriceSC = 50000;
        int room3DPriceOC = 100000;
        int room3DPriceSC = 70000;
        int room4DPrice = 150000;
        var nOB = Integer.parseInt(txtNumOfTicket.getText());
        if (roomType.equalsIgnoreCase("4D")) {
            var price = nOB * room4DPrice;
            txtMoviePriceReadOnly.setText(price + "");
        }

        if (cusType.equalsIgnoreCase("OC")) {
            if (roomType.equalsIgnoreCase("2D")) {
                var price = nOB * room2DPriceOC;
                txtMoviePriceReadOnly.setText(price + "");
            } else if (roomType.equalsIgnoreCase("3D")) {
                var price = nOB * room3DPriceOC;
                txtMoviePriceReadOnly.setText(price + "");
            }
        } else if (cusType.equalsIgnoreCase("SC")) {
            if (roomType.equalsIgnoreCase("2D")) {
                var price = nOB * room2DPriceSC;
                txtMoviePriceReadOnly.setText(price + "");
            } else if (roomType.equalsIgnoreCase("3D")) {
                var price = nOB * room3DPriceSC;
                txtMoviePriceReadOnly.setText(price + "");
            }
        }
    }
